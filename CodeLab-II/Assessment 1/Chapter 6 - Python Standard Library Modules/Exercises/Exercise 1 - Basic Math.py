# Using math library to do simple function
import math

# Given values
a1 = 2.3
a2 = 5
a3 = 16

# Calculate ceil and floor
ceil_result = math.ceil(a1)
floor_result = math.floor(a1)

# Calculate factorial
factorial_result = math.factorial(a2)

# Calculate exponentiation
exponentiation_result = 2 ** 3

# Calculate square root
sqrt_result = math.sqrt(a3)

# Print the results
print(f"For a = {a1}, ceil(a) is: {ceil_result}")
print(f"For a = {a1}, floor(a) is: {floor_result}")
print(f"For a = {a2}, the factorial is: {factorial_result}")
print(f"The value of 2^3 is: {exponentiation_result}")
print(f"For a = {a3}, the square root is: {sqrt_result}")

